/// \file Floor.h
/// \brief Creates and updates the floor of the game
/// \author Matthew Skellon
/// \version 1.0
/// \date 4/5/15 Initial Doxygen file creation, updated to NCCA Coding Standard

#ifndef FLOOR_H_
#define FLOOR_H_

#include <string>
#include "Scene.h"

class Floor
{
public :
    Floor()
    {
      ObjLoad("models/Floor.obj",
              "textures/FloorTexture.png",
              m_Vertex,
              m_Normal,
              m_Tex,
              m_v_Index,
              m_t_Index,
              m_n_Index,
              m_Texture);

      loadFloor();
    }
    ~Floor(){}

    //------------------------------------------------------------------------------------------------------------
    /// @brief Loads in the floor
    //------------------------------------------------------------------------------------------------------------
    void loadFloor();
    //------------------------------------------------------------------------------------------------------------
    /// @brief Updates the floor so it is drawn every frame
    //------------------------------------------------------------------------------------------------------------
    void update();

protected :
    //------------------------------------------------------------------------------------------------------------
    /// @brief Stores the vertex coordinates for loading
    //------------------------------------------------------------------------------------------------------------
    std::vector<Vec3> m_Vertex;
    //------------------------------------------------------------------------------------------------------------
    /// @brief Stores the normals for loading
    //------------------------------------------------------------------------------------------------------------
    std::vector<Vec3> m_Normal;
    //------------------------------------------------------------------------------------------------------------
    /// @brief Stores the texture coordinates for loading
    //------------------------------------------------------------------------------------------------------------
    std::vector<Vec3> m_Tex;
    //------------------------------------------------------------------------------------------------------------
    /// @brief Stores the face coordinates for loading
    //------------------------------------------------------------------------------------------------------------
    std::vector<Vec3> m_Face;
    //------------------------------------------------------------------------------------------------------------
    /// @brief Face vertices
    //------------------------------------------------------------------------------------------------------------
    std::vector<int>  m_v_Index;
    //------------------------------------------------------------------------------------------------------------
    /// @brief Face textures
    //------------------------------------------------------------------------------------------------------------
    std::vector<int>  m_t_Index;
    //------------------------------------------------------------------------------------------------------------
    /// @brief Face normals
    //------------------------------------------------------------------------------------------------------------
    std::vector<int>  m_n_Index;
    //------------------------------------------------------------------------------------------------------------
    /// @brief The texture
    //------------------------------------------------------------------------------------------------------------
    GLuint            m_Texture;
    //------------------------------------------------------------------------------------------------------------
    /// @brief Storage for some GL functions
    //------------------------------------------------------------------------------------------------------------
    std::vector<GLuint> m_DisplayList;
};

#endif
